#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n > 9 || n < -9)
		ft_putnbr_fd(n / 10, fd);
	if (n > -10 && n < 0)
		ft_putchar_fd('-', fd);
	ft_putchar_fd('0' + ((n > 0) * 2 - 1) * (n % 10), fd);
}
