#include "libft.h"

void	ft_putnbr(int n)
{
	if (n > 9 || n < -9)
		ft_putnbr(n / 10);
	if (n > -10 && n < 0)
		ft_putchar('-');
	ft_putchar('0' + ((n > 0) * 2 - 1) * (n % 10));
}
