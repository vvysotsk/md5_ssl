#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		j;
	size_t	k;

	i = 0;
	while (s1[i] != '\0')
		i++;
	j = 0;
	k = 0;
	while (s2[j] != '\0' && k < n)
	{
		s1[i] = s2[j];
		i++;
		j++;
		k++;
	}
	s1[i] = '\0';
	return (s1);
}
